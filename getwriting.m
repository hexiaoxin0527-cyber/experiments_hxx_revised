
function getwriting

% proj_path = fullfile('E:', 'isaac', 'dd-eft');
proj_path = get_project_path();

% Get participant ID
pt = readtable(fullfile(proj_path, 'participants.csv'));
pid = pt.id(end);
% pid = 1234;
unit = 0.12;
fs_title = 15;
fs_other = 12;

% Temporal prompts
temporal_prompts = {
    '未来几周内'
    '未来几个月内'
    '大约半年后'
    '大约一年后'
    '大约两年后'
};

% Get written responses
f = figure(...
    'MenuBar', 'none',...
    'Toolbar', 'none',...
    'Color', 'w',...
    'Tag', 'eft-fig');
    %'CloseRequestFcn', @(h, e) []);
f.UserData.idx = 1;
n_written = numel(temporal_prompts);
writing_template = struct(...
    'title', '',...
    'time', '',...
    'description', '');
f.UserData.writing = repmat(writing_template, 1, n_written);
while true
    if f.UserData.idx > n_written
        % Check that all the titles are unique
        if numel(unique({f.UserData.writing.title})) < n_written
            msgbox('看起来有重复的标题！请返回并确保每个事件都有一个独一无二的标题。');
            f.UserData.idx = n_written;
        else
            break
        end
    end
    uicontrol(f,...
        'Style', 'text',...
        'String', sprintf('%d/%d', f.UserData.idx, n_written),...
        'FontSize', fs_title,...
        'BackgroundColor', 'w',...
        'Units', 'normalized',...
        'Position', [0 1 - 0.1 1 0.1]);
    uicontrol(f,...
        'Style', 'text',...
        'String', sprintf('请描述一个会在%s发生的事件', temporal_prompts{f.UserData.idx}),...
        'FontSize', fs_other,...
        'BackgroundColor', 'w',...
        'Units', 'normalized',...
        'Position', [0.25 1-0.2 0.5 0.1]);
    p = uipanel(f,...
        'Units', 'normalized',...
        'BorderWidth', 1,...
        'BackgroundColor', 'w',...
        'Title', '标题',...
        'TitlePosition', 'centertop',...
        'FontSize', fs_other,...
        'Position', [0.2 1-3*unit 0.6 unit]);
    uicontrol(p,...
        'Style', 'edit',...
        'Tag', 'title',...
        'String', f.UserData.writing(f.UserData.idx).title,...
        'FontSize', fs_other,...
        'Units', 'normalized',...
        'Position', [0.01 0.01 0.98 0.98]);
    p = uipanel(f,...
        'Units', 'normalized',...
        'BorderWidth', 1,...
        'BackgroundColor', 'w',...
        'Title', '这个事件距离现在还有多少天？',...
        'TitlePosition', 'centertop',...
        'FontSize', fs_other,...
        'Position', [0.2 1-4*unit 0.6 unit]);
    uicontrol(p,...
        'Style', 'edit',...
        'Tag', 'time',...
        'String', f.UserData.writing(f.UserData.idx).time,...
        'FontSize', fs_other,...
        'Units', 'normalized',...
        'Position', [0.01 0.01 0.98 0.98]);
    p = uipanel(f,...
        'Units', 'normalized',...
        'BorderWidth', 1,...
        'BackgroundColor', 'w',...
        'Title', '描述',...
        'TitlePosition', 'centertop',...
        'FontSize', fs_other,...
        'Position', [0.1 1-7*unit 0.8 3*unit]);
    uicontrol(p,...
        'Style', 'edit',...
        'Tag', 'description',...
        'String', f.UserData.writing(f.UserData.idx).description,...
        'Max', inf,...
        'HorizontalAlignment', 'left',...
        'FontSize', fs_other,...
        'Units', 'normalized',...
        'Position', [0.01 0.01 0.98 0.98]);
    %{
    uicontrol(f,...
        'Style', 'text',...
        'String', '这个事件会在什么时候发生？',...
        'FontSize', fs,...
        'Units', 'normalized',...
        'Position', [0 1-3*unit 1 unit]);
    uicontrol(f,...
        'Style', 'edit',...
        'Tag', 'time',...
        'String', f.UserData.writing(f.UserData.idx).title,...
        'FontSize', fs,...
        'Units', 'normalized',...
        'Position', [0.25 1-4*unit 0.5 unit]);
    uicontrol(f,...
        'Style', 'text',...
        'String', '标题：',...
        'FontSize', fs,...
        'Units', 'normalized',...
        'Position', [0 1-5*unit 1 unit]);
    uicontrol(f,...
        'Style', 'edit',...
        'Tag', 'title',...
        'String', f.UserData.writing(f.UserData.idx).title,...
        'FontSize', fs,...
        'Units', 'normalized',...
        'Position', [0.25 1-6*unit 0.5 unit]);
    uicontrol(f,...
        'Style', 'text',...
        'String', '描述：',...
        'FontSize', fs,...
        'Units', 'normalized',...
        'Position', [0 1-7*unit 1 unit]);
    uicontrol(f,...
        'Style', 'edit',...
        'Tag', 'description',...
        'String', f.UserData.writing(f.UserData.idx).description,...
        'Max', inf,...
        'HorizontalAlignment', 'left',...
        'FontSize', fs,...
        'Units', 'normalized',...
        'Position', [0 unit 1 1-8*unit]);
    %}
    if f.UserData.idx == 1
        use_state = 'off';
    else
        use_state = 'on';
    end
    uicontrol(f,...
        'Style', 'pushbutton',...
        'String', sprintf('<<  上一个事件（%d/%d）', f.UserData.idx-1, n_written),...
        'BackgroundColor', 'w',...
        'Enable', use_state,...
        'Visible', use_state,...
        'FontSize', fs_other,...
        'Units', 'normalized',...
        'Position', [0.05 0.01 0.4 unit],...
        'Callback', @(h, e) increment(f, -1));
    if f.UserData.idx == n_written
        str = '完成';
    else
        str = '下一个事件  >>';
    end
    uicontrol(f,...
        'Style', 'pushbutton',...
        'String', str,...
        'BackgroundColor', 'w',...
        'FontSize', fs_other,...
        'Units', 'normalized',...
        'Position', [0.55 0.01 0.4 unit],...
        'Callback', @(h, e) increment(f, 1));
    uiwait(f);
end

% Save info
fid = fopen(fullfile(proj_path, 'data', sprintf('%d', pid), 'writing.txt'), 'w');
for w_idx = 1:numel(f.UserData.writing)
    fprintf(fid, 'title: %s\n', f.UserData.writing(w_idx).title);
    fprintf(fid, 'days: %s\n', f.UserData.writing(w_idx).time);
    description = f.UserData.writing(w_idx).description;
    description = reshape(description', 1, []);
    fprintf(fid, 'description: %s\n', description);
    fprintf(fid, '-end-\n');
end
fclose(fid);
delete(f);

end

function increment(f, n)

% Make sure there's a title and description
for tag = {'title' 'time' 'description'}
    obj = findobj(f, 'Tag', tag{:});
    str = obj.String;
    if isempty(str) && n > 0
        switch tag{:}
            case 'title'
                msg = '请填写标题。';
            case 'time'
                msg = '请填写距离现在的天数。';
            case 'description'
                msg = '请填写描述。';
            otherwise
                msg = '请填写该项。';
        end
        msgbox(msg);
        return
    end
    f.UserData.writing(f.UserData.idx).(tag{:}) = str;
end

% Make sure the title isn't too long
obj = findobj(f, 'Tag', 'title');
str = obj.String;
if numel(str) > 15
    msgbox('请确保标题不超过 15 个字符。');
    return
end

f.UserData.idx = f.UserData.idx + n;
uiresume(f);

end