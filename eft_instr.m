function eft_instr

% Display instructions for the EEG task

instr_txt = {
    '在实验的第一部分，您将被要求描述一系列未来可能发生的具体事件。这些事件需要满足：(1) 您本人会参与其中；(2) 是积极、愉快的；(3) 实际会发生或有现实可能发生；(4) 发生在某个具体地点，并且在一天之内完成。'
    '对于每个事件，您需要提供一个简短标题（最多 15 个字符）以及更详细的描述（至少 1 段）。在描述中，请尽可能提供丰富的细节。例如：您会在哪里？会看到什么？谁会在场？会发生什么？您会有什么感受？会想到什么？'
    '如有任何疑问，请随时询问实验人员。'
};
unit = 0.1;
fs = 15;
f = figure(...
    'MenuBar', 'none',...
    'Toolbar', 'none',...
    'Color', 'w',...
    'UserData', struct('txt', {instr_txt}, 'idx', 1));
    % 'CloseRequestFcn', @(h, e) []);
while true
    ud = f.UserData;
    if ud.idx > numel(ud.txt)
        break
    end
    uicontrol(f,...
        'Style', 'text',...
        'String', sprintf('%d/%d', ud.idx, numel(ud.txt)),...
        'FontSize', fs,...
        'BackgroundColor', 'w',...
        'Units', 'normalized',...
        'Position', [0 1-unit 1 unit]);
    uicontrol(f,...
        'Style', 'text',...
        'String', sprintf(ud.txt{ud.idx}),...
        'FontSize', fs,...
        'BackgroundColor', 'w',...
        'HorizontalAlignment', 'left',...
        'Units', 'normalized',...
        'Position', [0.05 unit 0.9 1-2*unit]);
    if f.UserData.idx == 1
        enable = 'off';
    else
        enable = 'on';
    end
    uicontrol(f,...
        'Style', 'pushbutton',...
        'String', '<<  上一页',...
        'Enable', enable,...
        'FontSize', fs,...
        'BackgroundColor', 'w',...
        'Units', 'normalized',...
        'Position', [0.05 0.01 0.4 unit],...
        'Callback', @(h, e) increment(f, -1));
    if ud.idx == numel(ud.txt)
        str = '开始任务';
    else
        str = '下一页  >>';
    end
    uicontrol(f,...
        'Style', 'pushbutton',...
        'String', str,...
        'FontSize', fs,...
        'BackgroundColor', 'w',...
        'Units', 'normalized',...
        'Position', [0.55 0.01 0.4 unit],...
        'Callback', @(h, e) increment(f, 1));
    uiwait(f);
end

delete(f);

end

function increment(f, n)

f.UserData.idx = f.UserData.idx + n;
uiresume(f);

end